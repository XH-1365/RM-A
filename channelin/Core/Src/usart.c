int _write (int fd, char *pBuffer, int size)  
{
  for (int i = 0; i < size; i++)  
  {
    while((USART2->SR&0X40)==0);//等待上一次串口数据发送完成  
    USART2->DR = (int8_t) *(pBuffer+i);       //写DR,串口2将发送数据
  }  
  return size;
}

